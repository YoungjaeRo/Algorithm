class Solution
{
   public int solution(int n, int a, int b)
	{
		/*
		 * 토너먼트는 한 라운드가 끝날 때마다
		 * 참가자의 번호가 다시 1번부터 재배정된다.
		 *
		 * 중요한 관찰:
		 *  - (1, 2)는 다음 라운드에서 1번
		 *  - (3, 4)는 다음 라운드에서 2번
		 *  - (5, 6)는 다음 라운드에서 3번
		 *
		 * 즉, 현재 번호 x는 다음 라운드에서
		 * (x + 1) / 2 번이 된다.
		 *
		 * A와 B가 같은 번호가 되는 순간,
		 * 같은 경기에서 만난 것이다.
		 */


		// 몇번째 라운드에서 만나게되는지
		int answer = 0;

		// 둘의 다음 라운드 번호가 같은 수가 될때까지 반복함
		while(a != b) {

			// 다음 라운드의 번호로 갱신해주기
			a = (a + 1) / 2;
			b = (b+ 1) / 2;

			answer++;
		}
		
		return answer;
	}
}