# 자동차 종류가 세단 또는 SUV 인 자동차 중에서, 2022/11/01 부터 2022/11/30까지 대여가 가능하고
# 30일간의 대여금액이 50만원 이상 200만원 미만인 자동차 출력
# 자동차 ID, 자동차 종류
SELECT C.CAR_ID, C.CAR_TYPE, FLOOR((C.DAILY_FEE * (100 - R.DISCOUNT_RATE) / 100) * 30) AS FEE
FROM CAR_RENTAL_COMPANY_CAR C
JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN R
ON C.CAR_TYPE = R.CAR_TYPE
AND R.DURATION_TYPE = '30일 이상'
WHERE C.CAR_TYPE IN('세단', 'SUV')

AND C.CAR_ID NOT IN(
SELECT CAR_ID
FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
WHERE START_DATE <= '2022-11-30' AND END_DATE >= '2022-11-01')

AND FLOOR((C.DAILY_FEE * (100 - R.DISCOUNT_RATE) / 100) * 30) >= 500000
AND FLOOR((C.DAILY_FEE * (100 - R.DISCOUNT_RATE) / 100) * 30) < 2000000
ORDER BY FEE DESC, C.CAR_TYPE ASC, C.CAR_ID DESC


